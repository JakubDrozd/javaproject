package com.example.basics.operators;

public class Op4_logical_operator_and {
    public static void main(String args[]){
        System.out.println(5 < 10 && 20 >= 15);
        System.out.println(5 > 10 && 20 > 15);
        System.out.println(4 > 2 && 10 == 11);
        System.out.println(4 == 2 && 12 > 20);
    }
}
